    public void borrarCamposAlumno() {
        CargarNombreAlumno.setText("");
        for (int i = 0; i < tablaMateriasAprobadas.getRowCount(); i++) {
            tablaMateriasAprobadas.setValueAt(false, i, 1);
        }
    }
    
    public boolean validarCamposCargaAlumno() {
        boolean esNombreValido = !CargarNombreAlumno.getText().equals("");
        return esNombreValido;
    }
    
    public boolean validarCamposInscripcionAlumno() throws SQLException {
        int legajo = (int)legajoAlumnoAInscribir.getValue();
        ArrayList resultado = ManejoDeDatos.buscarLineaIntegerGeneral("alumnos", "legajo", legajo);
        boolean existeInscripcion = false;
        for (int i = 0; i < tablaMateriasAprobadas.getRowCount(); i++) {
            if ((Boolean)tablaMateriasAprobadas.getValueAt(i, 1)) { // Columna "Seleccionar"
                existeInscripcion = true;
            }
        }
        if (!resultado.isEmpty()) {
            return true;
        }
        boolean esNombreValido = !CargarNombreAlumno.getText().equals("");
        return esNombreValido && existeInscripcion;
    }
    

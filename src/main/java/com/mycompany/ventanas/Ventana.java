    private void altaAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_altaAlumnoActionPerformed
        // TODO add your handling code here:
        tablaMateriasAprobadas.getColumnModel().getColumn(0).setPreferredWidth(principal.getWidth()-200);
        if (!ManejoDeDatos.verificarTabla("alumnos")) {
            String[] columnasACrear = new String[]{"legajo INT","nombre TEXT","aprobadas TEXT"};
            ManejoDeDatos.crearTabla("alumnos", columnasACrear);
        }
        if (ManejoDeDatos.verificarTabla("materias")) {
            try {
                Integrador.materias = new ArrayList<>(ManejoDeDatos.obtenerMaterias().values());
                this.refrescarTabla();
                panelVerMaterias.setVisible(false);
                panelVerAlumnos.setVisible(false);
                panelAltaMateria.setVisible(false);
                panelAltaAlumno.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else{
            String mensaje = "No podes cargar alumnos ya que no hay materias cargadas.";
            String mensaje2 = "por favor, cargue algunas y vuelva a intentar.";
            llamarModal(mensaje, mensaje2);
        }
    }//GEN-LAST:event_altaAlumnoActionPerformed


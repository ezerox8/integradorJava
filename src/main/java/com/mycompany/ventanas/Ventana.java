    public void refrescarMaterias(){
        try {
            JComboBox[] combos =  new JComboBox[]{listaMaterias, listaCorrelativas1, listaCorrelativas2, listaCorrelativas3, listaCorrelativas4, listaCorrelativas5};
            String[] response = ManejoDeDatos.consultarTabla("listadoMaterias", "materias");
            for(JComboBox comboBox : combos) {
                comboBox.setModel(new DefaultComboBoxModel<>(response));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.limpiarAltaMaterias();
    }
    
    public void refrescarTabla(){
        try {
            String[] materias = ManejoDeDatos.consultarTabla("listadoMaterias", "materias");
            DefaultTableModel modelo = (DefaultTableModel) tablaMateriasAprobadas.getModel();
            List<String> lista = new ArrayList(Integrador.materias.size());
            modelo.setRowCount(0);
            int i = 0;
            for(Materia materia : Integrador.materias){
                lista.add(materia.getNombre());
                i++;
            }
            for (String materia : materias) {
                if (lista.contains(materia)) {
                    modelo.addRow(new Object[]{materia, false});
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.limpiarAltaMaterias();
    }
    
    public void CargarTablaDeMateriasDadasDeAlta(){
        try {
            String[] materias = ManejoDeDatos.consultarTabla("listadoMaterias", "materias");
            DefaultTableModel modelo = (DefaultTableModel) tablaMateriasAprobadas.getModel();
            for (String materia : materias) {
                modelo.addRow(new Object[]{materia, false});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.limpiarAltaMaterias();
    }

